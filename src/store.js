export function fetchStockData({ unit }) {
    return new Promise((resolve, reject) => {
        setTimeout(() => {
            let response;

            if (unit === 'd') {
                response = mockIntradayStockData;
            } else if (unit === 'w') {
                response = mockWeekStockData;
            } else if (unit === 'm') {
                response = mockStockMonthData;
            } else {
                response = mockStockYearData;
            }

            resolve({ status: 200, data: response }); 

        }, 1000); 
    });
}
